/*
 * Exercício 2 - Operadores bit-a-bit
 * Exercício sobre os operadores que realizam operações diretamente em bits
 */

#include <stdio.h>

int main(void)
{
	printf("%d\n", 8 >> 2);
	printf("%d\n", 10 | 1);
	printf("%d\n", 9 << 4);
	printf("%d\n", 69 & 1);
	printf("%d\n", (11 >> 2) & 3);                                                                                                                                                                                                                                                                                                                                                                                                                               

	return 0;
}